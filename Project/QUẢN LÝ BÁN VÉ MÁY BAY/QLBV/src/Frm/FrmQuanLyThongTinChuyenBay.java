    public void LaydulieuChuyenBay() {
        String cautruyvan = "";
        cautruyvan = "SELECT * from tbChuyenBay ";
        ResultSet rs = Main.connection.ExcuteQueryGetTable(cautruyvan);
        Object[] obj = new Object[]{"Mã Chuyến Bay", "Mã Máy Bay", "Trạng Thái", "Mã Lộ Trình","Mã Lịch Bay","Mã Tuyến Bay"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        jTable1.setModel(tableModel);
        try {
            while (rs.next()) {
                Object[] item = new Object[6];
                item[0] = rs.getString("MaCB");
                item[1] = rs.getString("MaMB");
                item[2] = rs.getString("TrangThai");
                item[3] = rs.getString("lt.MaLT");
                item[4] = rs.getString("lb.MaLB");
                item[5] = rs.getString("tb.MaTB");             
                tableModel.addRow(item);
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }    
        
    }
    
