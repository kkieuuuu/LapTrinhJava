    public boolean KiemTraNhapChuyenBay(int ts) {
        boolean kiemtra = false;
        String MaCB,MaMB,TrangThai,MaLT,MaLB,MaTB;
        MaCB=txtMaCB.getText();
        MaMB=txtMaMB.getText();
        TrangThai=txtTrangThai.getText();
        MaLT=txtMaLT.getText();
        MaLB=txtMaLB.getText();
        MaTB=txtMaTB.getText();
        String  ThongBao = "";
        
        if (MaCB.equals("") && ts == 1) {
            ThongBao += "Bạn Chưa nhập Mã Chuyến Bay\n";
            lblMaCB.setForeground(Color.red);
        }
        if (MaMB.equals("")) {
            ThongBao += "Bạn chưa Nhập Mã Máy Bay\n";
            lblMaMB.setForeground(Color.red);
        }
        if (TrangThai.equals("")) {
            lblTrangThai.setForeground(Color.red);
            ThongBao += "Bạn chưa Nhập Trạng thái chuyến bay\n";
        }
        if (MaLT.equals("")) {
            lblMaLT.setForeground(Color.red);
            ThongBao += "Bạn chưa Nhập Mã Lộ Trình\n";
           
        }
        if (MaLB.equals("")) {
            ThongBao += "Bạn chưa Nhập Mã Lộ Trình\n";
            lblMaLB.setForeground(Color.red);
        }
        if (MaTB.equals("")) {
            ThongBao += "Bạn chưa Nhập Mã Tuyến Bay\n";
            lblMaTB.setForeground(Color.red);
        }
        if (ThongBao.equals("")) {
            kiemtra = true;
            lblMaCB.setForeground(Color.black);
            lblMaMB.setForeground(Color.black);
            lblTrangThai.setForeground(Color.black);
            lblMaLT.setForeground(Color.black);
            lblMaLB.setForeground(Color.black);
            lblMaTB.setForeground(Color.black);
        } else {
            kiemtra = false;
            frmTC.ThongBao(ThongBao, "LỖI NHẬP LIỆU", 2);
        }
        return kiemtra;    
        }
    
